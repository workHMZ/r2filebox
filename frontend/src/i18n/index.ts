const currentLocale = ref<Locale>(detectLocale())

export const locale = computed(() => currentLocale.value)

export function initI18n() {
  applyDocumentLanguage(currentLocale.value)
}

export function setLocale(nextLocale: Locale) {
  currentLocale.value = nextLocale
  localStorage.setItem(STORAGE_KEY, nextLocale)
  applyDocumentLanguage(nextLocale)
}

export function getLocaleTag(nextLocale: Locale = currentLocale.value): string {
  if (nextLocale === 'zh') return 'zh-CN'
  if (nextLocale === 'ja') return 'ja-JP'
  return 'en-US'
}

export function t(key: string, params: Record<string, string | number> = {}): string {
  const template = messages[currentLocale.value][key] || messages.zh[key] || key
  return template.replace(/\{(\w+)\}/g, (_, name) => String(params[name] ?? `{${name}}`))
}

export function useI18n() {
  return {
    locale,
    languageOptions,
    setLocale,
    t,
  }
}

function detectLocale(): Locale {
  const saved = localStorage.getItem(STORAGE_KEY)
  if (isLocale(saved)) return saved

  const languages = navigator.languages?.length ? navigator.languages : [navigator.language]
  for (const lang of languages) {
    const normalized = lang.toLowerCase()
    if (normalized.startsWith('zh')) return 'zh'
    if (normalized.startsWith('ja')) return 'ja'
    if (normalized.startsWith('en')) return 'en'
  }
  return 'en'
}

function isLocale(value: string | null): value is Locale {
  return value === 'zh' || value === 'en' || value === 'ja'
}

function applyDocumentLanguage(nextLocale: Locale) {
  document.documentElement.lang = getLocaleTag(nextLocale)
}
